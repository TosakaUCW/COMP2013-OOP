package question4;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = 49;

        // Check if the number is odd or even
        if (number % 2 == 1) {
            System.out.println("Odd Number");
        } else {
            System.out.println("Even Number");
        }

        System.out.println("BYE!");
    }
}